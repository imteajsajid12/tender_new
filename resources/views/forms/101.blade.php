			<div><b><span class="ucase">(2)</span></b> <b>משכורת חודש</b> – משכורת בעד עבודה שלא פחות מ-18 יום בחודש ויותר מ-5 כמספר שעות בכל יום.</div>
			<div><b><span class="ucase">(3)</span></b> <b>משכורת נוספת</b> – משכורת בעד עבודה שלא פחות מ- 18 יום בחודש ויותר מ-5 כמספר שעות בכל יום, נוסף למשכורת חודש ו/או בנוסף לקצבה החייבת במס ממקום אחר. העובד רשאי לבחור את מקום העבודה בו תחשב משכורתו "כמשכורת נוספת".</div>

			<div><b><span class="ucase">(4)</span></b> <b>משכורת חלקית</b> – משכורת בעד עבודה במשך 5 שעות או פחות בכל יום או משכורת בעד עבודה במשך יותר מ-5 שעות ביום אך לא יותר מ-8 שעות בשבוע. ממשכורת חלקית ינוכה מס בשיעור מרבי אלא אם כן זו הכנסה יחידה שאז ינוכה מס לפי לוח הניכויים.</div>

			<div><b><span class="ucase">(5)</span></b> <b>שכר עבודה</b> – משכורת בעד עבודה של יותר מ – 5 שעות ביום אך פחות מ – 18 יום בחודש. משכר עבודה ינוכה מס לפי לוח יומי אלא אם כן זו הכנסה יחידה שאז ינוכה מס לפי לוח הניכויים.</div>

			<div><b><span class="ucase">(6)</span></b> <b>קצבה</b> – מקצבה שהיא הכנסה יחידה ינוכה מס לפי לוח הניכויים. אם יש הכנסות נוספות – ינוכה מס בשיעור מרבי או על פי תיאום מס מפקיד השומה.</div>

<div><b><span class="ucase">(7)</span></b> אם העובד לא מילא משבצת זו – המעביד מנוע מלנכות מס לפי לוח הניכויים ויש לנכות מס מרבי לפי התקנות מכל תשלומי המעביד.</div>

<div><b><span class="ucase">(8)</span></b> אם העובד מילא משבצת זו – המעביד מנוע מלנכות מס לפי לוח הניכויים ויש לנכות מס מרבי לפי התקנות.</div>

<div><b><span class="ucase">(9)</span></b> אם העובד לא מילא משבצת זו – על המעביד לצרף למשכורת את סכומי ההפרשות לקרן השתלמות ולנכות מס לפי התקנות או לפעול לפי אישור תיאום מס מפקיד השומה.</div>

<div><b><span class="ucase">(10)</span></b> אם העובד לא מילא משבצת זו – על המעביד לצרף למשכורת את סכומי ההפרשות לקצבה/ לאובדן כושר עבודה ולנכות מס לפי התקנות או לפעול לפי אישור תיאום מס מפקיד השומה.</div>		
